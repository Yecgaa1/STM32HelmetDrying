/**
  * @brief This function handles USART2 global interrupt / USART2 wake-up interrupt through EXTI line 26.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  if(__HAL_UART_GET_IT_SOURCE(&huart2,UART_IT_IDLE)!=RESET){
    __HAL_UART_CLEAR_IDLEFLAG(&huart2);
    HAL_UART_DMAStop(&huart2);
    int recCNT=recLen-__HAL_DMA_GET_COUNTER(huart2.hdmarx);
    char tmp[100]="";
    strncpy(tmp,rec,recCNT);
    if (recCNT==3) {
      if (strncmp(rec,"hot",3)==0) {
        //启动加热
        Hot();
      }
    }
    else if (recCNT==4) {
      if (strncmp(rec,"stop",4)==0) {
        //停止所有
        Stop();
      }
      else if (strncmp(rec,"hotC",4)==0) {
        //继续加热前先校验
        if (HAL_GPIO_ReadPin(DoorIN_GPIO_Port,DoorIN_Pin) == GPIO_PIN_RESET) {
          TJCSendAny("noWork.en=1");
          TJCSendAny("WorkingTIM.en=0");
          TJCSendTxt("t5","继续");
        }
        else {
          Hot();
        }
      }
    }else if (recCNT==5) {
      if (strncmp(rec,"light",5)==0) {
        //启动紫外
        Light();
      }
    }
    else if (recCNT==6) {
      if (strncmp(rec,"lightC",6)==0) {
        //继续紫外前先校验
        if (HAL_GPIO_ReadPin(DoorIN_GPIO_Port,DoorIN_Pin) == GPIO_PIN_RESET) {
          TJCSendAny("noWork.en=1");
          TJCSendAny("WorkingTIM.en=0");
          TJCSendTxt("t5","继续");
        }
        else {
          Light();
        }
      }
    }
    memset(rec,0,recLen);
    HAL_UART_Receive_DMA(&huart2,(uint8_t*)rec,recLen);
  }
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

